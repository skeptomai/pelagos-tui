    // -----------------------------------------------------------------------
    // Subscription event application
    // -----------------------------------------------------------------------

    pub fn apply_subscription(&mut self, msg: SubscriptionMsg) {
        if let Some(rx) = &self.status_rx {
            while let Ok(msg) = rx.try_recv() {
                self.status_message = Some(msg);
            }
        }
        self.last_any_msg = Instant::now();

        match msg {
            SubscriptionMsg::Snapshot {
                containers,
                vm_running,
            } => {
                self.containers = if self.show_all {
                    containers
                } else {
                    containers
                        .into_iter()
                        .filter(|c| c.status == "running")
                        .collect()
                };
                // Drop selections for containers no longer present.
                let present: HashSet<String> =
                    self.containers.iter().map(|c| c.name.clone()).collect();
                self.selected_names.retain(|n| present.contains(n));
                clamp_selected(&mut self.selected, self.containers.len());
                self.vm_running = vm_running;
                self.last_refresh = Instant::now();
                self.subscription_reconnecting = false;
                self.status_message = None;
            }

            SubscriptionMsg::ContainerStarted { container } => {
                if self.show_all || container.status == "running" {
                    if let Some(pos) = self
                        .containers
                        .iter()
                        .position(|c| c.name == container.name)
                    {
                        self.containers[pos] = *container;
                    } else {
                        self.containers.push(*container);
                    }
                }
                self.last_refresh = Instant::now();
            }

            SubscriptionMsg::ContainerExited { name, .. } => {
                if !self.show_all {
                    self.containers.retain(|c| c.name != name);
                    self.selected_names.remove(&name);
                    clamp_selected(&mut self.selected, self.containers.len());
                } else if let Some(c) = self.containers.iter_mut().find(|c| c.name == name) {
                    c.status = "exited".to_string();
                }
                self.last_refresh = Instant::now();
            }

            SubscriptionMsg::Heartbeat { .. } => {
                self.last_refresh = Instant::now();
            }

            SubscriptionMsg::Disconnected => {
                self.vm_running = false;
                self.subscription_reconnecting = true;
            }
        }
    }

    pub fn subscription_stale(&self) -> bool {
        !self.subscription_reconnecting && self.last_any_msg.elapsed().as_secs() > 15
    }

    // -----------------------------------------------------------------------
    // Key handling
    // -----------------------------------------------------------------------

    pub fn on_key(&mut self, key: KeyEvent) {
        match self.mode {
            Mode::Normal => self.on_key_normal(key),
            Mode::Images => self.on_key_images(key),
            Mode::ImagePull => self.on_key_image_pull(key),
            Mode::ImageInspect => self.on_key_image_inspect(key),
            Mode::ProfilePicker => self.on_key_profile_picker(key),
            Mode::CommandPalette => self.on_key_palette(key),
            Mode::Confirm => self.on_key_confirm(key),
            Mode::ConfirmQuit => self.on_key_confirm_quit(key),
            Mode::Inspect => self.on_key_inspect(key),
        }
    }

    fn on_key_normal(&mut self, key: KeyEvent) {
        match key.code {
            KeyCode::Char('q') => self.mode = Mode::ConfirmQuit,
            KeyCode::Char('c') if key.modifiers.contains(KeyModifiers::CONTROL) => {
                self.should_quit = true;
            }

            KeyCode::Char('j') | KeyCode::Down => {
                if !self.containers.is_empty() {
                    self.selected = (self.selected + 1).min(self.containers.len() - 1);
                }
            }
            KeyCode::Char('k') | KeyCode::Up => {
                self.selected = self.selected.saturating_sub(1);
            }

            // Space: toggle multi-select on highlighted row, then advance cursor.
            KeyCode::Char(' ') => {
                if let Some(c) = self.containers.get(self.selected) {
                    let name = c.name.clone();
                    if !self.selected_names.remove(&name) {
                        self.selected_names.insert(name);
                    }
                    if self.selected + 1 < self.containers.len() {
                        self.selected += 1;
                    }
                }
            }

            KeyCode::Char('a') => {
                self.show_all = !self.show_all;
                self.selected = 0;
                self.selected_names.clear();
                if let Some(cfg) = &self.sub_config {
                    let mut c = cfg.lock().unwrap();
                    c.generation = c.generation.wrapping_add(1);
                }
            }

            KeyCode::Char('p') => {
                self.profile_picker_selected = self
                    .profiles
                    .iter()
                    .position(|p| p == &self.profile)
                    .unwrap_or(0);
                self.mode = Mode::ProfilePicker;
            }

            KeyCode::Char('r') => {
                self.palette_input = format!(
                    "--name {} -i {} {}",
                    random_name(),
                    self.tui_config.default_image,
                    self.tui_config.default_it_cmd,
                );
                self.mode = Mode::CommandPalette;
            }

            KeyCode::Char('R') => {
                self.palette_input = format!("--name {} -d ", random_name());
                self.mode = Mode::CommandPalette;
            }

            // Container actions — operate on multi-selection or highlighted row.
            KeyCode::Char('s') => self.begin_action(ConfirmAction::Stop),
            KeyCode::Char('S') => self.begin_action(ConfirmAction::Restart),
            KeyCode::Char('d') => self.begin_action(ConfirmAction::Remove),

            // Inspect overlay: show full detail for highlighted container.
            KeyCode::Enter | KeyCode::Char('i') => {
                if let Some(c) = self.containers.get(self.selected) {
                    self.pending_inspect = Some(c.name.clone());
                    self.inspect_scroll = 0;
                    self.mode = Mode::Inspect;
                }
            }

            // Images screen.
            KeyCode::Char('I') => {
                self.images_selected = 0;
                self.images_error = None;
                self.pending_image_ls = true;
                self.images_loading = true;
                self.mode = Mode::Images;
            }

            // Prune: target all exited containers regardless of selection.
            KeyCode::Char('P') => {
                let targets: Vec<String> = self
                    .containers
                    .iter()
                    .filter(|c| c.status != "running")
                    .map(|c| c.name.clone())
                    .collect();
                if targets.is_empty() {
                    self.status_message = Some("no exited containers to prune".to_string());
                } else {
                    self.confirm_action = Some(ConfirmAction::Remove);
                    self.confirm_targets = targets;
                    self.mode = Mode::Confirm;
                }
            }

            _ => {}
        }
    }

    fn on_key_profile_picker(&mut self, key: KeyEvent) {
        match key.code {
            KeyCode::Char('j') | KeyCode::Down => {
                if !self.profiles.is_empty() {
                    self.profile_picker_selected =
                        (self.profile_picker_selected + 1).min(self.profiles.len() - 1);
                }
            }
            KeyCode::Char('k') | KeyCode::Up => {
                self.profile_picker_selected = self.profile_picker_selected.saturating_sub(1);
            }
            KeyCode::Enter => {
                if let Some(chosen) = self.profiles.get(self.profile_picker_selected).cloned() {
                    if chosen != self.profile {
                        let old = std::mem::replace(&mut self.profile, chosen.clone());
                        log::debug!("profile switch: {} -> {}", old, chosen);
                        self.containers.clear();
                        self.selected = 0;
                        self.selected_names.clear();
                        self.tui_config = TuiConfig::load(&chosen);
                        // Update the target profile but do NOT bump generation yet.
                        // main.rs will stop the old VM first, then bump, so the
                        // new VM starts without a NAT relay port conflict.
                        if let Some(cfg) = &self.sub_config {
                            cfg.lock().unwrap().profile = chosen;
                        }
                        self.pending_profile_switch = Some(old);
                    }
                }
                self.mode = Mode::Normal;
            }
            KeyCode::Esc | KeyCode::Char('p') => {
                self.mode = Mode::Normal;
            }
            _ => {}
        }
    }

    fn on_key_palette(&mut self, key: KeyEvent) {
        match key.code {
            KeyCode::Esc => {
                self.palette_input.clear();
                self.mode = Mode::Normal;
            }
            KeyCode::Enter => {
                let input = self.palette_input.trim().to_string();
                if !input.is_empty() {
                    self.pending_run = Some(input);
                }
                self.palette_input.clear();
                self.mode = Mode::Normal;
            }
            KeyCode::Backspace => {
                self.palette_input.pop();
            }
            KeyCode::Char(c) => {
                self.palette_input.push(c);
            }
            _ => {}
        }
    }

    fn on_key_confirm(&mut self, key: KeyEvent) {
        match key.code {
            KeyCode::Char('y') | KeyCode::Char('Y') => {
                if let Some(action) = self.confirm_action.take() {
                    let targets = std::mem::take(&mut self.confirm_targets);
                    if action == ConfirmAction::ImageRm {
                        self.pending_image_rm = targets.into_iter().next();
                        self.mode = Mode::Images;
                    } else {
                        self.pending_action = Some((action, targets));
                        self.selected_names.clear();
                        self.mode = Mode::Normal;
                    }
                }
            }
            _ => {
                // Any key other than 'y' cancels — return to whichever screen initiated.
                let from_images = self.confirm_action == Some(ConfirmAction::ImageRm);
                self.confirm_action = None;
                self.confirm_targets.clear();
                self.mode = if from_images {
                    Mode::Images
                } else {
                    Mode::Normal
                };
            }
        }
    }

    fn on_key_confirm_quit(&mut self, key: KeyEvent) {
        match key.code {
            KeyCode::Char('y') | KeyCode::Char('Y') | KeyCode::Char('q') => {
                self.should_quit = true;
            }
            _ => {
                self.mode = Mode::Normal;
            }
        }
    }

    fn on_key_inspect(&mut self, key: KeyEvent) {
        match key.code {
            KeyCode::Esc | KeyCode::Char('q') => {
                self.mode = Mode::Normal;
                self.inspect_container = None;
            }
            KeyCode::Char('j') | KeyCode::Down => {
                self.inspect_scroll = self.inspect_scroll.saturating_add(1);
            }
            KeyCode::Char('k') | KeyCode::Up => {
                self.inspect_scroll = self.inspect_scroll.saturating_sub(1);
            }
            _ => {}
        }
    }

    fn on_key_images(&mut self, key: KeyEvent) {
        match key.code {
            KeyCode::Esc | KeyCode::Char('I') => {
                self.mode = Mode::Normal;
            }
            KeyCode::Char('j') | KeyCode::Down => {
                if !self.images.is_empty() {
                    self.images_selected = (self.images_selected + 1).min(self.images.len() - 1);
                }
            }
            KeyCode::Char('k') | KeyCode::Up => {
                self.images_selected = self.images_selected.saturating_sub(1);
            }
            KeyCode::Char('r') => {
                self.images_loading = true;
                self.images_error = None;
                self.pending_image_ls = true;
            }
            // Open the run command palette with the selected image pre-filled.
            KeyCode::Char('R') => {
                if let Some(img) = self.images.get(self.images_selected) {
                    self.palette_input = format!("--name {} -d {}", random_name(), img.reference,);
                    self.mode = Mode::CommandPalette;
                }
            }
            KeyCode::Char('p') => {
                self.image_pull_input.clear();
                self.mode = Mode::ImagePull;
            }
            KeyCode::Char('d') => {
                if let Some(img) = self.images.get(self.images_selected) {
                    self.confirm_action = Some(ConfirmAction::ImageRm);
                    self.confirm_targets = vec![img.reference.clone()];
                    self.mode = Mode::Confirm;
                }
            }
            KeyCode::Enter => {
                if let Some(img) = self.images.get(self.images_selected) {
                    self.pending_image_inspect = Some(img.reference.clone());
                    self.image_inspect_lines.clear();
                    self.image_inspect_loading = true;
                    self.image_inspect_scroll = 0;
                    self.mode = Mode::ImageInspect;
                }
            }
            _ => {}
        }
    }

    fn on_key_image_pull(&mut self, key: KeyEvent) {
        match key.code {
            KeyCode::Esc => {
                self.image_pull_input.clear();
                self.mode = Mode::Images;
            }
            KeyCode::Enter => {
                let input = self.image_pull_input.trim().to_string();
                if !input.is_empty() {
                    self.pending_image_pull = Some(input);
                }
                self.image_pull_input.clear();
                self.mode = Mode::Images;
            }
            KeyCode::Backspace => {
                self.image_pull_input.pop();
            }
            KeyCode::Char(c) => {
                self.image_pull_input.push(c);
            }
            _ => {}
        }
    }

    fn on_key_image_inspect(&mut self, key: KeyEvent) {
        match key.code {
            KeyCode::Esc | KeyCode::Char('q') => {
                self.image_inspect_lines.clear();
                self.mode = Mode::Images;
            }
            KeyCode::Char('j') | KeyCode::Down => {
                self.image_inspect_scroll = self.image_inspect_scroll.saturating_add(1);
            }
            KeyCode::Char('k') | KeyCode::Up => {
                self.image_inspect_scroll = self.image_inspect_scroll.saturating_sub(1);
            }
            _ => {}
        }
    }

    // -----------------------------------------------------------------------
